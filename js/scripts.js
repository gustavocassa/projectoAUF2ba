$(document).ready(function() {
    $('.contenedorLlamados1').click(function() {
        // Encuentra todos los enlaces dentro del div
        var enlaces = $(this).find('a');

        // Abre una nueva pestaña en el navegador para cada enlace
        for (var i = 0; i < enlaces.length; i++) {
            window.open(enlaces[i].href, '_blank');
        }
    });
});

